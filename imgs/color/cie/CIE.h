 public:
  /* CIE refererence illuminant enumeration
   */
  enum ReferenceIlluminant { a = 0, d65 = 1 };

  /* CIE standard observer enumeration
   */
  enum StandardObserver { two_deg = 0, ten_deg = 1 };

  /* Constructor
   */
  CIE() {}

  /* CIE wavelengths getter
   */
  Eigen::VectorXd wavelengths() const { return wavelengths_; }

  /* CIE reference illuminant getter
   */
  Eigen::VectorXd reference_illuminant(
      int illuminant = ReferenceIlluminant::d65) const {
    Eigen::VectorXd reference_illuminant = Eigen::VectorXd(471);
    switch (illuminant) {
      case ReferenceIlluminant::a:
        for (int w_idx = 0; w_idx < 471; w_idx++) {
          reference_illuminant[w_idx] = reference_illuminant_a_[w_idx];
        }
        break;
      case ReferenceIlluminant::d65:
        for (int w_idx = 0; w_idx < 471; w_idx++) {
          reference_illuminant[w_idx] = reference_illuminant_d65_[w_idx];
        }
        break;
    }
    return reference_illuminant;
  }

  /* CIE standard observer getter
   */
  Eigen::MatrixXd standard_observer(
      int observer = StandardObserver::ten_deg) const {
    Eigen::MatrixXd standard_observer = Eigen::MatrixXd(471, 3);
    for (int w_idx = 0; w_idx < 471; w_idx++) {
      for (int r_idx = 3 * observer; r_idx < 3 * observer + 3; r_idx++) {
        standard_observer(w_idx, r_idx) = standard_observer_[w_idx * 6 + r_idx];
      }
    }
    return standard_observer;
  }

  /* CIE standard observer (x-bar) getter
   */
  Eigen::VectorXd xbar(int observer = StandardObserver::ten_deg) const {
    Eigen::VectorXd response = Eigen::VectorXd(471);
    int r_idx = 3 * observer + 0;
    for (int w_idx = 0; w_idx < 471; w_idx++) {
      response(w_idx) = standard_observer_[w_idx * 6 + r_idx];
    }
    return response;
  }

  /* CIE standard observer (y-bar) getter
   */
  Eigen::VectorXd ybar(int observer = StandardObserver::ten_deg) const {
    Eigen::VectorXd response = Eigen::VectorXd(471);
    int r_idx = 3 * observer + 1;
    for (int w_idx = 0; w_idx < 471; w_idx++) {
      response(w_idx) = standard_observer_[w_idx * 6 + r_idx];
    }
    return response;
  }

  /* CIE standard observer (z-bar) getter
   */
  Eigen::VectorXd zbar(int observer = StandardObserver::ten_deg) const {
    Eigen::VectorXd response = Eigen::VectorXd(471);
    int r_idx = 3 * observer + 2;
    for (int w_idx = 0; w_idx < 471; w_idx++) {
      response(w_idx) = standard_observer_[w_idx * 6 + r_idx];
    }
    return response;
  }

